public class chw_04 {
    public static void main(String[] args) {
        byte age=34;
        char ch='A';
        long ageDino=5666666666666L; // for long, Double and float we use initial character in capital or small like l or L at last to declare it as that datatype
        float f1= 1.5f;
        double d1=4.66D;
        System.out.println(age);
        System.out.println(ch);
        System.out.println(ageDino);
        System.out.println(d1);
        System.out.println(f1);
    }
}